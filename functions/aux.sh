# ================================= > max < ================================== #

function max {
    max=0
    for x in ${@}; do
        if [[ $x -gt $max ]]; then
            max=$x
        fi
    done
    echo $max
}

# ────────────────────────────────── <end> ─────────────────────────────────── #

# ================================= > rep < ================================== #

function rep {
    for ((i = 1; i <= $2; i++)); do
        printf -- "${1}"
    done
    echo ''
}

